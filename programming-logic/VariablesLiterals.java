public class VariablesLiterals {
    public static void main(String[] args) {
        float salary = 2500.0f; // f for float
        double extra = 300.5d; // d for double
        long lightSpeed = 21002203238L; // L for long
        int age = 20;

        System.out.println(salary +" "+ extra +" "+ lightSpeed +" "+ age);
    }
}
